let fruits = ['mango', 'orange', 'banana', 'pineapple'];
console.log(fruits);

// Adding elements to the end of array
fruits.push('Kivi');
console.log(fruits);

// Adding elements to the Beginning of the array
fruits.unshift('Apple');
console.log(fruits);

// Middle
fruits.splice(2, 0, 'VOD');
console.log(fruits);
