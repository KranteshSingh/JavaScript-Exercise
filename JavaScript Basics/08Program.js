let x = 4;
let y = 10;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);

// Increment(++)
console.log(++x); // First value will be incremented and then value will display
console.log(x++); // First value will be displayed and then value will be increased

//Decrement(--)
console.log(--y); // First value will be decremented and then value will display
console.log(y--); // First value will be displayed and then value will be decreased
